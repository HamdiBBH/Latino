import { MetadataRoute } from "next";

export default function sitemap(): MetadataRoute.Sitemap {
    const baseUrl = "https://latinocoucoubeach.com";
    const now = new Date();

    return [
        {
            url: baseUrl,
            lastModified: now,
            changeFrequency: "weekly",
            priority: 1,
        },
        {
            url: `${baseUrl}/reservation`,
            lastModified: now,
            changeFrequency: "daily",
            priority: 0.9,
        },
    ];
}
